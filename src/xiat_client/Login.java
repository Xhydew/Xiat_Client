    private void lee() {
        server = entradaIP.getText();
        nick = entradaNombre.getText();
        if (server.isEmpty() || nick.isEmpty()) {//falta comprobacion de puerto vacio
            error("Completa los dos campooos!!", "Letrasao!");
        } else {
            if (!validaPuerto()) {
                error("El puerto no es validoo!", "Puerto mal");
            } else {
                if (!compruebaIP(server)) {
                    error("La ip no es valida!!!!", "IP mal");
                } else {
                    this.setVisible(false);
                    datos = true;
                    cliente.getLogin();
                }
            }
        }
    }

    private void error(String error, String titulo) {
        JOptionPane.showMessageDialog(this,
                error,
                titulo,
                JOptionPane.ERROR_MESSAGE);
    }

    private boolean compruebaIP(String ip) {
        String arr[];
        arr = ip.split("\\.");
        if (arr.length != 4) {
            return false;
        }
        for (String x : arr) {
            try {
                int numero = Integer.parseInt(x);
                if (numero > 254 || numero < 0) {
                    return false;
                }
            } catch (Exception e) {
                return false;
            }

        }
        return true;
    }

    private boolean validaPuerto() {
        try {
            puerto = Integer.parseInt(entradaPuerto.getText());
        } catch (Exception e) {
            return false;
        }
        if (puerto <= 0) {
            return false;
        }
        return true;
    }

