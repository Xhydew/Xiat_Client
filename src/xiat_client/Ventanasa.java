    private void leer() {
        String txt;
        txt = texto.getText();

        escribe(txt, 0);
        fuera.println(txt);
        fuera.flush();

        texto.setText(null);
    }

    public void escribe(String texto, int x) {
        /*        if (x == 0) {
         cuadro.setAlignmentX(RIGHT_ALIGNMENT);
         }*/
        /// (int x == 0)? cuadro.append("Enviado") : cuadro.append("Ensviado");      
        cuadro.append((x == 0) ? "Enviado: " + texto + "\n" : "Recibido: " + texto + "\n");
    }

